/* Seção 2 - Aula 7: Console.log*/

// Exibe mensagens e variáveis no console do JavaScript

// --------- STRINGS ---------

// Texto precisa ser colocado entre ' ou "
console.log('Paulo Vítor');
console.log("Paulo Vítor");

// As crases ` ` também podem ser usadas
console.log(`Paulo Vítor`);

// Aspas simples podem ser usadas dentro de aspas duplas e vice-versa
console.log("'Paulo' Vítor");
console.log('Paulo "Vítor"');

// Aspas simples e duplas podem ser usadas dentro de crases
console.log(`'Paulo' "Vítor"`);

//--------- NÚMEROS ---------

// Não há a necessidade de usar aspas ou crases na hora de exibir números
console.log(696969);

// Números decimais (ponto flutuante) também podem ser exibidos
console.log(3.1415);

// Múltiplas coisas podem ser enviadas ao mesmo tempo num console.log, para fazer isso deve-se separar os valores selecionados por vírgula (,) e espaço ( )
console.log("Paulo", 19, 65.87);